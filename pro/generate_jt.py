SIZE_PARAMS = {
    'small': {
        'glyphs':      GLYPHS_SMALL,
        'char_width':  6,
        'char_height': 8,
        'char_gap':    1,
        'bits':        6,
    },
    'large': {
        'glyphs':      GLYPHS_LARGE,
        'char_width':  10,
        'char_height': 14,
        'char_gap':    1,
        'bits':        10,
    },
}

PANEL_W = 96
PANEL_H = 16

CUSTOM_WIDTHS = {
    ('large', ':'): (4, 4),
    ('large', ','): (4, 4),
}

COLORS = {
    'red':     (255,   0,   0),
    'green':   (  0, 255,   0),
    'blue':    (  0,   0, 255),
    'magenta': (255,   0, 255),
    'yellow':  (255, 255,   0),
    'cyan':    (  0, 255, 255),
    'white':   (255, 255, 255),
}

def text_to_pixels(text, color, size='large'):
    start_x = 3 if size == 'small' else 5
    start_y = 5 if size == 'small' else 2

    p = SIZE_PARAMS[size]
    glyphs     = p['glyphs']
    char_width = p['char_width']
    char_gap   = p['char_gap']
    bits       = p['bits']

    pixels = [[(0, 0, 0)] * PANEL_W for _ in range(PANEL_H)]
    cx = start_x

    for ch in text.upper():
        if ch not in glyphs:
            print(f"  Avertissement : '{ch}' non supporté, ignoré.", file=sys.stderr)
            continue

        if (size, ch) in CUSTOM_WIDTHS:
            ch_bits, ch_width = CUSTOM_WIDTHS[(size, ch)]
        else:
            ch_bits, ch_width = bits, char_width

        glyph = glyphs[ch]
        for row_idx, byte in enumerate(glyph):
            for bit in range(ch_bits - 1, -1, -1):
                col = cx + (ch_bits - 1 - bit)
                r = start_y + row_idx
                if 0 <= col < PANEL_W and 0 <= r < PANEL_H:
                    if (byte >> bit) & 1:
                        pixels[r][col] = color
        cx += ch_width + char_gap
        if cx >= PANEL_W:
            break

    return pixels


def pixels_to_graffiti(pixels):
    r_plane = [0] * 192
    g_plane = [0] * 192
    b_plane = [0] * 192
    for col in range(PANEL_W):
        for row in range(PANEL_H):
            pr, pg, pb = pixels[row][col]
            byte_idx = col * 2 + row // 8
            bit_idx  = 7 - (row % 8)
            if pr: r_plane[byte_idx] |= (1 << bit_idx)
            if pg: g_plane[byte_idx] |= (1 << bit_idx)
            if pb: b_plane[byte_idx] |= (1 << bit_idx)
    return r_plane + g_plane + b_plane


def build_jt(graffiti_data):
    return [{
        "data": {
            "graffitiData": graffiti_data,
            "graffitiType": 1,
            "mode":         1,
            "pixelHeight":  PANEL_H,
            "pixelWidth":   PANEL_W,
            "speed":        255,
        },
        "dataType": 1,
    }]


def generate_jt(text, color="red", size='large'):
    pixels        = text_to_pixels(text, parse_color(color), size)
    graffiti_data = pixels_to_graffiti(pixels)
    obj           = build_jt(graffiti_data)

    with open("generated.jt", 'w') as f:
        json.dump(obj, f, separators=(',', ':'))

def parse_color(value):
    v = value.strip().lower()
    if v in COLORS:
        return COLORS[v]
    raise argparse.ArgumentTypeError(
        f"Couleur invalide : '{value}'. "
        f"Utilisez un nom ({', '.join(COLORS)})."
    )